    public void cargar(int contador) {
        new Thread() {
            public void run() {
                for (int i = contador; i <= 702; i += 4) {
                    try {
                        lbCargar.setBounds(0, 0, i, 4);
                        this.sleep(0, 2);
                    } catch (InterruptedException ex) {

                    }
                }
                lbCargar.setBounds(0, 0, 0, 4);
            }
        }.start();
    }

    public void cargarTabla(String busqueda) {
        tbPisos.getTableHeader().setFont(new Font("Tahoma", Font.BOLD, 13));
        tbPisos.getTableHeader().setOpaque(false);
        tbPisos.getTableHeader().setBackground(new Color(32, 136, 203));
        tbPisos.getTableHeader().setForeground(new Color(255, 255, 255));
        ((DefaultTableCellHeaderRenderer) tbPisos.getTableHeader().getDefaultRenderer()).setHorizontalAlignment(JLabel.CENTER);
        pisos = new ControladorPiso();
        tbPisos.setModel(this.pisos.tablaPisos(busqueda));
        cellRenderer.setHorizontalAlignment(SwingConstants.CENTER);
        tbPisos.getColumnModel().getColumn(0).setCellRenderer(cellRenderer);
    }

    public void insertarPisos() {
        cargar(20);
        this.pisos.insertarPisos(piso.getText(), Notificacion, Mensaje);
    }

    public void actualizarPisos() {
        cargar(20);
        this.pisos.actualizarPisos(piso.getText(), pisoID, Notificacion, Mensaje);
    }

    public void eliminarPisos() {
        cargar(20);
        this.pisos.eliminarPisos(pisoID, Notificacion, Mensaje);
    }

    public boolean verificar() {
        boolean campo = true;
        if (piso.getText().contains("Ingrese") || piso.getText().equals("")) {
            campo = false;
            ErrorPiso.setVisible(true);
        }
        return campo;
    }

    public void mensaje() {
        Notificacion.setVisible(true);
        Notificacion.setBackground(new Color(220, 24, 24));
        Mensaje.setText("Verifique que los campos estan llenados");
    }

    public void habilitarBotones() {
        AgregarModificar.setToolTipText("Modificar");
        AgregarModificar.setIcon(new ImageIcon("src/Imagen/Principal/modificar-24.png"));
        AgregarModificar.setBackground(new Color(255, 181, 62));
        Eliminar.setEnabled(true);
    }

    public void cerrarNotificacion() {
        Notificacion.setVisible(false);
    }

    public void cancelar() {
        AgregarModificar.setToolTipText("Agregar");
        AgregarModificar.setIcon(new ImageIcon("src/Imagen/Principal/agregar-24.png"));
        AgregarModificar.setBackground(new Color(35, 144, 35));
        Eliminar.setEnabled(false);
        piso.setText("Ingrese el nombre del piso");
        buscar.setText("Buscar un piso");
        ErrorPiso.setVisible(false);
        cargarTabla("");
    }

    public void seleccionar(int seleecion) {
        habilitarBotones();
        this.pisos.listaPisos().stream().filter((objetoPiso) -> (objetoPiso.getPisoID().equals(tbPisos.getValueAt(seleecion, 0)))).forEachOrdered((objetoPiso) -> {
            piso.setText(objetoPiso.getPisoID());
            pisoID = objetoPiso.getPisoID();
        });
    }

